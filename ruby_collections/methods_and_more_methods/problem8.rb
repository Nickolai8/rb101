# the take method on an array takes an integer argument. take creates
# a new array to return, and fills that array by using the integer provided
# as an argument to decide how many elements to use from the array object
# that take was called on (starting from the very first element). 
# arr = [1, 2, 3, 4, 5]
# arr.take(2)
# this code would therefore take 2 elements from the 'arr' array, being
# the first two, 1 and 2, and store them in a new array and return it.
# take is not destructive, therefore the calling object is not mutated.
# we can find out how take works by reading the ruby documentation. We
# can also just test it by writing a short program or using irb in the
# terminal.
